/**
  14- pedir que se digite un número e indicar si es par o impar.
*/

let v = prompt ("Ingrese un número, el cual desee saber si es par o impar: ")

if(v % 2 == 0){
  alert("El número " + v + " Es par")
}
else if(v & 2 != 0){
  alert("El número " + v + " es impar")
}

